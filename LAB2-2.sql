-- 1. Bảng PHONGBAN (Tạo trước để NHANVIEN tham chiếu tới)
CREATE TABLE PHONGBAN (
    MAPHG INT PRIMARY KEY,
    TENPHG NVARCHAR2(50),
    TRPHG CHAR(9),
    NG_NHANCHUC DATE
);

-- 2. Bảng NHANVIEN
CREATE TABLE NHANVIEN (
    HONV NVARCHAR2(15),
    TENLOT NVARCHAR2(15),
    TENNV NVARCHAR2(15),
    MANV CHAR(9) PRIMARY KEY,
    NGSINH DATE,
    DCHI NVARCHAR2(50),
    PHAI NVARCHAR2(3),
    LUONG NUMBER(10,2),
    MA_NQL CHAR(9),
    PHG INT,
    CONSTRAINT FK_NV_PB FOREIGN KEY (PHG) REFERENCES PHONGBAN(MAPHG)
);

-- 3. Bảng DIADIEM_PHG
CREATE TABLE DIADIEM_PHG (
    MAPHG INT,
    DIADIEM NVARCHAR2(30),
    PRIMARY KEY (MAPHG, DIADIEM),
    CONSTRAINT FK_DD_PB FOREIGN KEY (MAPHG) REFERENCES PHONGBAN(MAPHG)
);

-- 4. Bảng DEAN
CREATE TABLE DEAN (
    MADA INT PRIMARY KEY,
    TENDA NVARCHAR2(50),
    DDIEM_DA NVARCHAR2(50),
    PHONG INT,
    CONSTRAINT FK_DA_PB FOREIGN KEY (PHONG) REFERENCES PHONGBAN(MAPHG)
);

-- 5. Bảng PHANCONG
CREATE TABLE PHANCONG (
    MA_NVÊN CHAR(9),
    SODA INT,
    THOIGIAN NUMBER(5,1),
    PRIMARY KEY (MA_NVÊN, SODA),
    CONSTRAINT FK_PC_NV FOREIGN KEY (MA_NVÊN) REFERENCES NHANVIEN(MANV),
    CONSTRAINT FK_PC_DA FOREIGN KEY (SODA) REFERENCES DEAN(MADA)
);

-- 6. Bảng THANNHAN
CREATE TABLE THANNHAN (
    MA_NVÊN CHAR(9),
    TENTN NVARCHAR2(50),
    PHAI NVARCHAR2(3),
    NGSINH DATE,
    QUANHE NVARCHAR2(20),
    PRIMARY KEY (MA_NVÊN, TENTN),
    CONSTRAINT FK_TN_NV FOREIGN KEY (MA_NVÊN) REFERENCES NHANVIEN(MANV)
);

